package com.game.domain.s;

import java.util.List;

public class StaticOfficer {
    private int officerId;
    private int level;
    private int makeIron;
    private int makeCopper;
    private int makeOil;
    private int makeStone;
    private int molecularPro;
    private int denominatorPro;
    private int lastTime;
    private int gold;
    private List<Integer> resource;

    public int getOfficerId() {
        return officerId;
    }

    public void setOfficerId(int officerId) {
        this.officerId = officerId;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getMakeIron() {
        return makeIron;
    }

    public void setMakeIron(int makeIron) {
        this.makeIron = makeIron;
    }

    public int getMakeCopper() {
        return makeCopper;
    }

    public void setMakeCopper(int makeCopper) {
        this.makeCopper = makeCopper;
    }

    public int getMakeOil() {
        return makeOil;
    }

    public void setMakeOil(int makeOil) {
        this.makeOil = makeOil;
    }

    public int getMakeStone() {
        return makeStone;
    }

    public void setMakeStone(int makeStone) {
        this.makeStone = makeStone;
    }

    public int getMolecularPro() {
        return molecularPro;
    }

    public void setMolecularPro(int molecularPro) {
        this.molecularPro = molecularPro;
    }

    public int getDenominatorPro() {
        return denominatorPro;
    }

    public void setDenominatorPro(int denominatorPro) {
        this.denominatorPro = denominatorPro;
    }

    public int getLastTime() {
        return lastTime;
    }

    public void setLastTime(int lastTime) {
        this.lastTime = lastTime;
    }

    public int getGold() {
        return gold;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }

    public List<Integer> getResource() {
        return resource;
    }

    public void setResource(List<Integer> resource) {
        this.resource = resource;
    }

}
