package com.game.domain.s;

public class StaticWashSkill {
    private int washType;
    private int skillLv;
    private int rate;
    private int equipQuality;
    private int washLimit;

    public int getWashType() {
        return washType;
    }

    public void setWashType(int washType) {
        this.washType = washType;
    }

    public int getSkillLv() {
        return skillLv;
    }

    public void setSkillLv(int skillLv) {
        this.skillLv = skillLv;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }

    public int getEquipQuality() {
        return equipQuality;
    }

    public void setEquipQuality(int equipQuality) {
        this.equipQuality = equipQuality;
    }

    public int getWashLimit() {
        return washLimit;
    }

    public void setWashLimit(int washLimit) {
        this.washLimit = washLimit;
    }
}
