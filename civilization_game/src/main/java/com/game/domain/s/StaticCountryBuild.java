public class StaticCountryBuild {

	private int buildId;
	private int countryLv;
	private int count;
	private long iron;
	private long copper;
	private long oil;
	private int countryExp;
	private long prestige;

	public int getBuildId() {
		return buildId;
	}

	public void setBuildId(int buildId) {
		this.buildId = buildId;
	}

	public int getCountryLv() {
		return countryLv;
	}

	public void setCountryLv(int countryLv) {
		this.countryLv = countryLv;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public long getIron() {
		return iron;
	}

	public void setIron(long iron) {
		this.iron = iron;
	}

	public long getCopper() {
		return copper;
	}

	public void setCopper(long copper) {
		this.copper = copper;
	}

	public long getOil() {
		return oil;
	}

	public void setOil(long oil) {
		this.oil = oil;
	}

	public int getCountryExp() {
		return countryExp;
	}

	public void setCountryExp(int countryExp) {
		this.countryExp = countryExp;
	}

	public long getPrestige() {
		return prestige;
	}

	public void setPrestige(long prestige) {
		this.prestige = prestige;
	}

}
