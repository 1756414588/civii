package com.game.domain.s;

public class StaticCountryGovern {
	private int governId;
	private int count;
	private int propId;
	private int propNum;
	private int person;
	private int power;

	public int getGovernId() {
		return governId;
	}

	public void setGovernId(int governId) {
		this.governId = governId;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public int getPropId() {
		return propId;
	}

	public void setPropId(int propId) {
		this.propId = propId;
	}

	public int getPropNum() {
		return propNum;
	}

	public void setPropNum(int propNum) {
		this.propNum = propNum;
	}

	public int getPerson() {
		return person;
	}

	public void setPerson(int person) {
		this.person = person;
	}

	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

}
