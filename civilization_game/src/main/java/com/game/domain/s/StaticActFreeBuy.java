package com.game.domain.s;

public class StaticActFreeBuy {

	private int footId;
	private int awardId;
	private int type;
	private int sortId;
	private int price;
	private String name;

	public int getFootId() {
		return footId;
	}

	public void setFootId(int footId) {
		this.footId = footId;
	}

	public int getAwardId() {
		return awardId;
	}

	public void setAwardId(int awardId) {
		this.awardId = awardId;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getSortId() {
		return sortId;
	}

	public void setSortId(int sortId) {
		this.sortId = sortId;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
