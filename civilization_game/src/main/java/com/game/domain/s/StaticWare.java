package com.game.domain.s;


public class StaticWare {
    private int wareLevel;
    private long iron;
    private long copper;
    private long oil;

    public int getWareLevel () {
        return wareLevel;
    }

    public void setWareLevel (int wareLevel) {
        this.wareLevel = wareLevel;
    }

    public long getIron () {
        return iron;
    }

    public void setIron (long iron) {
        this.iron = iron;
    }

    public long getCopper () {
        return copper;
    }

    public void setCopper (long copper) {
        this.copper = copper;
    }

    public long getOil () {
        return oil;
    }

    public void setOil (long oil) {
        this.oil = oil;
    }
}
