package com.game.domain.s;

// 模拟玩家配置
public class StaticWorldPlayer {
    private int mapId;
    private int x;
    private int y;

    public int getMapId () {
        return mapId;
    }

    public void setMapId (int mapId) {
        this.mapId = mapId;
    }

    public int getX () {
        return x;
    }

    public void setX (int x) {
        this.x = x;
    }

    public int getY () {
        return y;
    }

    public void setY (int y) {
        this.y = y;
    }
}
