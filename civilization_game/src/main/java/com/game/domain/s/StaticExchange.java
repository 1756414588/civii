package com.game.domain.s;

public class StaticExchange {
    private int keyId;
    private int resOutType;
    private int resInType;
    private int percent;

    public int getKeyId() {
        return keyId;
    }

    public void setKeyId(int keyId) {
        this.keyId = keyId;
    }

    public int getResOutType() {
        return resOutType;
    }

    public void setResOutType(int resOutType) {
        this.resOutType = resOutType;
    }

    public int getResInType() {
        return resInType;
    }

    public void setResInType(int resInType) {
        this.resInType = resInType;
    }

    public int getPercent() {
        return percent;
    }

    public void setPercent(int percent) {
        this.percent = percent;
    }
}
