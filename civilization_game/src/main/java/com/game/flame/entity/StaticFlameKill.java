package com.game.flame.entity;

public class StaticFlameKill {

    private int id;
    private int cond;
    private long res;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCond() {
        return cond;
    }

    public void setCond(int cond) {
        this.cond = cond;
    }

    public long getRes() {
        return res;
    }

    public void setRes(long res) {
        this.res = res;
    }
}
