package com.game.season.turn.entity;

public class StaticTurnConfig {
	private int id;
	private int freeTimes;
	private int onePrice;
	private int tenPrice;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getFreeTimes() {
		return freeTimes;
	}

	public void setFreeTimes(int freeTimes) {
		this.freeTimes = freeTimes;
	}

	public int getOnePrice() {
		return onePrice;
	}

	public void setOnePrice(int onePrice) {
		this.onePrice = onePrice;
	}

	public int getTenPrice() {
		return tenPrice;
	}

	public void setTenPrice(int tenPrice) {
		this.tenPrice = tenPrice;
	}
}
