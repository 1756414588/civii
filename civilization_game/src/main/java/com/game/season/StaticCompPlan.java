package com.game.season;

public class StaticCompPlan {

	private int compseasonPlanId;
	private int activityId;
	private int awardId;
	private int openWeekBegin;
	private int weekDay;
	private int openDays;
	private int previewDays;
	private int showDays;
	private int mergeNum;
	private String name;

	public int getCompseasonPlanId() {
		return compseasonPlanId;
	}

	public void setCompseasonPlanId(int compseasonPlanId) {
		this.compseasonPlanId = compseasonPlanId;
	}

	public int getActivityId() {
		return activityId;
	}

	public void setActivityId(int activityId) {
		this.activityId = activityId;
	}

	public int getAwardId() {
		return awardId;
	}

	public void setAwardId(int awardId) {
		this.awardId = awardId;
	}

	public int getOpenWeekBegin() {
		return openWeekBegin;
	}

	public void setOpenWeekBegin(int openWeekBegin) {
		this.openWeekBegin = openWeekBegin;
	}

	public int getWeekDay() {
		return weekDay;
	}

	public void setWeekDay(int weekDay) {
		this.weekDay = weekDay;
	}

	public int getOpenDays() {
		return openDays;
	}

	public void setOpenDays(int openDays) {
		this.openDays = openDays;
	}

	public int getPreviewDays() {
		return previewDays;
	}

	public void setPreviewDays(int previewDays) {
		this.previewDays = previewDays;
	}

	public int getShowDays() {
		return showDays;
	}

	public void setShowDays(int showDays) {
		this.showDays = showDays;
	}

	public int getMergeNum() {
		return mergeNum;
	}

	public void setMergeNum(int mergeNum) {
		this.mergeNum = mergeNum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
